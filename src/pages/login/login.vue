<template>
    <div class="login-container">
        <el-form ref="loginForm" :model="loginForm" :rules="loginRules" class="login-form" autocomplete="on" label-position="left">

        <div class="title-container">
            <h3 class="title">Login Form</h3>
        </div>

        <el-form-item prop="userName">
            <span class="svg-container">
            <svg-icon icon-class="user" />
            </span>
            <el-input
            ref="userName"
            v-model="loginForm.userName"
            placeholder="用户名"
            name="userName"
            type="text"
            tabindex="1"
            autocomplete="on"
            />
        </el-form-item>

        <el-tooltip v-model="capsTooltip" content="Caps lock is On" placement="right" manual>
            <el-form-item prop="password">
            <span class="svg-container">
                <svg-icon icon-class="password" />
            </span>
            <el-input
                :key="passwordType"
                ref="password"
                v-model="loginForm.password"
                :type="passwordType"
                placeholder="密码"
                name="password"
                tabindex="2"
                autocomplete="on"
                @keyup.native="checkCapslock"
                @blur="capsTooltip = false"
                @keyup.enter.native="handleLogin"
            />
            <span class="show-pwd" @click="showPwd">
                <svg-icon :icon-class="passwordType === 'password' ? 'eye' : 'eye-open'" />
            </span>
            </el-form-item>
        </el-tooltip>

        <el-button :loading="loading" type="primary" style="width:100%;margin-bottom:30px;" @click.native.prevent="submit">Login</el-button>

        <div style="position:relative">
            <div class="tips">
                <span>用户名 : superAdmin</span>
                <span>密码 : any</span>
            </div>
            <div class="tips">
                <span style="margin-right:18px;">用户名 : editor</span>
                <span>密码 : any</span>
            </div>
            <div class="tips">
                <span style="margin-right:18px;">用户名 : any</span>
                <span>密码 : any</span>
            </div>
        </div>
        </el-form>
    </div>
</template>

<script>
    export default {
        name: 'Login',
        data() {
            const validatePassword = (rule, value, callback) => {
            if (value.length < 6) {
                callback(new Error('密码至少6位数'))
            } else {
                callback()
            }
            }
            return {
            loginForm: {
                userName: 'admin',
                password: '111111'
            },
            loginRules: {
                userName: [{ required: true, trigger: 'blur', message: '请输入用户名'}],
                password: [{ required: true, trigger: 'blur', validator: validatePassword }]
            },
            passwordType: 'password',
            capsTooltip: false,
            loading: false,
            otherQuery: {}
            }
        },
        mounted() {
            if (this.loginForm.userName === '') {
            this.$refs.userName.focus()
            } else if (this.loginForm.password === '') {
            this.$refs.password.focus()
            }
        },
        methods: {
            checkCapslock(e) {
                const { key } = e
                this.capsTooltip = key && key.length === 1 && (key >= 'A' && key <= 'Z')
            },
            showPwd() {
                if (this.passwordType === 'password') {
                    this.passwordType = ''
                } else {
                    this.passwordType = 'password'
                }
                this.$nextTick(() => {
                    this.$refs.password.focus()
                })
            },
            submit() {
                this.$refs.loginForm.validate(valid => {
                    if (valid) {
                        this.loading = true
                        this.login();
                    }
                })
            },
            login(){
                this.$http.post('/login',this.loginForm).then(([success,res])=>{
                    console.log('登录')
                    console.log(success,res)
                    if(success){
                        let role = '';
                        if(this.loginForm.userName === 'superAdmin'){
                            role = '2';
                        }else if(this.loginForm.userName === 'admin'){
                            role = '1';
                        }else{
                            role = '0';
                        }
                        sessionStorage.setItem('accountToken', res.body.token);
                        sessionStorage.setItem('userName', this.loginForm.userName);
                        sessionStorage.setItem('role',role);
                        this.$store.commit('user/setRelo', role)
                        this.$store.commit('user/setToken', res.body.token)
                        this.$router.push({path:'/'});
                    }
                    this.loading = false;
                })
            },
        }
    }
</script>

